<h1 class="titre mi-court"><span>KI, Kesako?</span></h1>

<div id="principal">
	<p>
		Qu'est-ce que le KI ? Eh bien, le KI (prononcer K-I) est le <span class="surligne">Club Informatique de l'École des Ponts ParisTech</span>. Fondé en 1997, le KI est un incontounable de la vie étudiante aux Ponts ! En effet nous touchons à tout ce qui se rapporte à l'informatique et ferons notre possible pour t'offrir une expérience virtuelle agréable et dynamique.
	</p>
	<p>
		Ainsi, que tu veuilles jouer à des jeux vidéo (Wii-U et ordinateurs toujours prêts), en apprendre plus (grâce à nos formations), emprunter du matériel, ou résoudre un problème informatique, tu peux toujours pousser la porte de notre local !
	</p>
	<p>
		Nous développons également, entre autres projets, l'intranet étudiant des Ponts. Nous nous ferons un plaisir de t'accueillir pour te faire découvrir nos activités, que tu puisses en profiter et si tu le souhaites t'y essayer au développement web !
	</p>
	<p>
		Tu l'auras compris nous sommes motivés par le désir d'apprendre, le goût des nouvelles technologies et l'envie d'aider !
	</p>
</div>