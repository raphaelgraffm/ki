<p> Animée par l'envie de servir son prochain tout en étant à la pointe des nouvelles technologies,
l'équipe du Club Informatique des Ponts et Chaussées (KI) prête une oreille attentive 
à toutes tes questions ou remarques sur le numérique. </p>

<p> Le KI c'est aussi un groupe de gens motivés réunis autour de projets ambitieux,
qui n'hésitent pas à sacrifier de leur temps de sommeil pour offrir aux élèves des Ponts
la meilleure expérience virtuelle possible. </p>